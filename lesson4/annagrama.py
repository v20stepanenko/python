# Напишем приложение для игры в анаграмму:
# Правила этой игры довольно старые и простые. Человеку предоставляется слово из набора букв, которые он должен расставить таким образом, чтобы получилось правильное слово.
# Кпримеру:
# ооаркв - корова
# тмале - метла
# Способом перестановки букв, находят и составляют корректное слово.

# Программа заранее должна содержать набор слов для анаграммы, достаточно будет 3-4 слов для проверки.
# Пользователю выводят слово из хаотичного набора букв. Все, что может делать пользователь, это указывать какую букву, куда переместить.
# Кпримеру:
# тмале
# Вторую букву, поставим первой
# мтале
# теперь пятую букву, поставим второй
# метал
# теперь четвертую букву, поставим пятой
# метла

# Программа предоставляет пользовательский ввод, куда пользователь указывает через пробел номер буквы, которую нужно переместить и номер буквы, на который нужно переместить букву: 3 1 - третью букву поставить первой, 1 4 - первую букву поставить четвертой и Т.Д.
# Когда пользователь выстроит все буквы правильно, программа сообщает ему о том, что он молодец и составил слово правильно, затем предлогает ему следующее слово и так до тех пор, пока заготовленные слова не закончатся.

