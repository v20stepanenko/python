class Anagram_word:

    def __init__(self, word, anagram):
        self._word = word
        self.anagram = anagram        

    def check_word(self):
        return self._word == self.anagram

    def move_char(self, mov_command):
        position = mov_command.split(' ')
        last = len(self.anagram)
        if position[0].isdigit() and position[1].isdigit():
            pos1 = int(position[0]) -1
            pos2 = int(position[1]) -1
        if last >= pos1 >= 0 and last >= pos2 >= 0:
            unimmutable = list(self.anagram)
            unimmutable[pos1], unimmutable[pos2] = unimmutable[pos2], unimmutable[pos1]
            self.anagram = ''.join(unimmutable)
        else: 
            print('Input wrong move command')

    def play(self):
        while True:
            if not self.check_word():
                print('Введите команду для смены буквы (индекс - позиция). \nАнаграмма - %s.' %self.anagram)
                input_move = input()
                self.move_char(input_move)
            else:
                print("Вы выиграли, слово: %s" % self._word)
                return

list_anagram = (Anagram_word('сорт', 'трос'), 
                Anagram_word('программа', 'мрроагмап'),
                Anagram_word('чертог', 'еогрчт'))

list_anagram[1].play()